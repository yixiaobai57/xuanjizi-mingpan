def get_hour_zhi(hour: int) -> str:
    return DI_ZHI[HOUR_TO_ZHI.get(hour, 0)]

def get_shi_shen(day_gan: str, target_gan: str) -> str:
    return SHI_SHEN_TABLE.get((day_gan, target_gan), '未知')

def get_wu_xing_distribution(bazi: dict) -> dict:
    dist = {'木':0,'火':0,'土':0,'金':0,'水':0}
    for pillar in ['year','month','day','hour']:
        gan = bazi[pillar]['gan']
        zhi = bazi[pillar]['zhi']
        dist[GAN_WU_XING.get(gan,'')] = dist.get(GAN_WU_XING.get(gan,''), 0) + 1
        dist[ZHI_WU_XING.get(zhi,'')] = dist.get(ZHI_WU_XING.get(zhi,''), 0) + 1
    return dist
